"""
Harmonic-distortion separation with exponential sweeps (Farina / Novak).

A single exponential sine sweep characterises the linear response and
every harmonic distortion order of a weakly nonlinear system at once:
after deconvolution, the distortion products of order ``n``
pack into separate impulse responses that *precede* the linear response by
the fixed advance (Farina, AES 108th Convention, 2000)

``dt_n = T * ln(n) / ln(f2/f1) = L * ln(n)``,

where ``L = T / ln(f2/f1)`` is the sweep rate. Windowing each arrival
yields the **higher harmonic impulse responses** ``h_n(t)`` and their
Fourier transforms, the **higher harmonic frequency responses** ``H_n(f)``
(Novak, Lotton & Simon, JAES 63(10), 2015, Sec. 1.3), from which the
harmonic distortion is read as a function of the excitation frequency with
one measurement instead of a tone-by-tone sweep.

Two excitation/deconvolution pairs are implemented:

* **Synchronized swept-sine (Novak et al. 2015)** -- the sweep
  ``x(t) = sin[2*pi*f1*L*exp(t/L)]`` with the rate rounded so that
  ``f1*L`` is an integer (Eqs. 47/49). Time-shifting this signal by
  ``L*ln(n)`` is then *exactly* equivalent to generating its n-th
  harmonic (Eq. 18), so the phases of the ``H_n(f)`` measure the system
  instead of the excitation. The impulse response is deconvolved with the
  closed-form spectrum of the inverse filter (Eq. 51) rather than an FFT
  of the signal, which extends the usable band of ``H_n`` to
  ``[n*f1, n*f2]`` (their Fig. 6) and avoids inverting spectral ripple.
  Generated by :func:`synchronized_sweep_signal`; analysed with
  ``method="synchronized"`` (the default).

* **Exponential sweep with the Farina inverse filter** -- the classical
  ESS of :func:`phonometry.sweep_signal` deconvolved by the time-reversed,
  amplitude-compensated sweep (Farina 2000; ISO 18233:2006 Figure B.2,
  the machinery of :func:`phonometry.impulse_response`). The harmonic
  *magnitudes* are correct, but the sweep is not synchronized: its ``-1``
  phase term breaks the time-shift/harmonic equivalence, so the phases of
  ``H_n`` for ``n >= 2`` depend on the excitation and are not meaningful
  (Novak et al. 2015, Sec. 2.4 and Fig. 7). Use ``method="farina"`` to
  analyse recordings made with the existing ESS.

The memoryless polynomial oracle anchors both paths: driving
``y = x + a2*x**2 + a3*x**3`` with a unit sweep must return, by the
Chebyshev identities, ``|H1| = 1 + 3*a3/4``, ``|H2| = a2/2`` (phase
``-pi/2``) and ``|H3| = a3/4`` (phase ``pi``), and a THD(f) equal to the
closed form ``sqrt((a2/2)**2 + (a3/4)**2) / (1 + 3*a3/4)`` -- which also
matches :func:`phonometry.thd` measured tone by tone on the same system.
"""

